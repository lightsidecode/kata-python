# Arytmetyka i typy danych w jezyku Python.

x, y = 5, 2     # operator przypisania, x = 5 oraz y = 2

print(x + y)    # dodawanie
print(x - y)    # odejmowanie
print(x * y)    # mnozenie
print(x / y)    # dzielenie zmiennoprzecinkowe (wynik = 2.5)
print(x // y)   # dzielenie calkowitoliczbowe (wynik = 2)
print(x % 2)    # dzielenie modulo (reszta z dzielenia = 1)
print(x ** y)   # potegowanie (x^y)

# mozna stosowac operatpra przypisania w polaczeniu z operatorami
# arytmetycznymi np.:

z = x
z += y          # rownowaznie: z = z + y
